/** Find a professional by id, or undefined. */
export function getProfessionalById(id) {
  return professionals.find((p) => p.id === id);
}

/** Find a firm by id, or undefined. */
export function getFirmById(id) {
  return firms.find((f) => f.id === id);
}

/** Return all reviews for a given professional id. */
export function getReviewsByProfessional(id) {
  return reviews.filter((r) => r.professionalId === id);
}

/** Return all professionals belonging to a given firm id. */
export function getProfessionalsByFirm(firmId) {
  return professionals.filter((p) => p.firmId === firmId);
}
